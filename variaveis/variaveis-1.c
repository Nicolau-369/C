#include <stdio.h>
#include <stdlib.h>
int main(){
    int a = 125;//decimal value
    int b = 0435;//value in octal, equivalent to 285
    int c = 0xFA;//value in hexadecimal, equivalent to 506
    printf("Value of a: %d\n",a);
    printf("Value of b: %d\n",b);
    printf("Value of c: %d\n",c);
    system("pause");
    return 0;
}
